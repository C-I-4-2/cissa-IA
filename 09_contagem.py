# Define o valor inicial da contagem
contador = 5

# Inicia o laço while que continuará enquanto o contador for maior que 0
while contador > 0:
    print(contador)  # Exibe o valor atual
    contador -= 1     # Decrementa 1 do contador a cada repetição

# Após a contagem regressiva, exibe a mensagem final
print("Lançar!")