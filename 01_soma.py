''' 1 - Crie um programa em Python que peça dois números ao usuário, some esses valores e exiba o resultado na tela '''

# Programa para somar dois números inseridos pelo usuário

# Solicita o primeiro número ao usuário e converte para tipo float
numero1 = float(input("Digite o primeiro número: "))

# Solicita o segundo número ao usuário e converte para tipo float
numero2 = float(input("Digite o segundo número: "))

# Realiza a soma dos dois números
soma = numero1 + numero2

# Exibe o resultado da soma
print("A soma dos dois números é:", soma)


