# Define uma lista com alguns números
numeros = [5, 10, 3, 8, 2]

# Inicializa uma variável para armazenar a soma
soma_total = 0

# Percorre cada número da lista usando um laço for
for numero in numeros:
    soma_total += numero  # Adiciona o número atual à soma_total

# Exibe o resultado final
print("A soma total dos números é:", soma_total)